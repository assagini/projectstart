package org.zerock.concept;

public class GET_POST {
/*
게시물의 등록 페이지를 보여준다	
GET /board/register 

게시물을 실제로 등록한다
POST /board/register

특정 번호의 게시물을 조회한다.
GET /board/read?bno=xx

게시물의 수정 화면으로 이동한다.
GET /board/mod?bno=xx

게시물을 수정한다.
POST /board/mod

게시물을 삭제한다.
POST /board/remove

게시물의 목록을 확인한다.
GET /board/list
	
*/	
}
